console.log('WHILE')
let contador = 1
while (contador <= 6) {
    console.log(`Passo ${contador}`)
    contador++
}

console.log('\nDO WHILE')
contador = 1
do {
    console.log(`Passo ${contador}`)
    contador++
} while (contador <= 6)


